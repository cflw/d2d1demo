#include "图形.h"
#include "常量.h"
void C图形::f初始化(HWND a窗口) {
	m二维.f初始化(a窗口, c缩放);
	m画文本 = m二维.fc画文本();
	m效果 = m二维.fc文本效果();
	auto &v文本工厂 = m二维.fg文本工厂();
	二维::S文本格式参数 v格式;
	v格式.fs字号(48);
	v格式.fs水平对齐(二维::E文本水平对齐::e中);
	m布局 = v文本工厂.fc文本布局(L"我人有的和主产不为这\n工要在地一上是中国\nz经以发了民同" ,v格式);
}
void C图形::f销毁() {
}
